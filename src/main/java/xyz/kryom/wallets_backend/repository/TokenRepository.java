package xyz.kryom.wallets_backend.repository;

import java.util.Optional;
import org.springframework.data.jpa.repository.JpaRepository;
import org.springframework.data.jpa.repository.Query;
import org.springframework.stereotype.Repository;
import xyz.kryom.wallets_backend.model.Token;

/**
 * @author Tomas Toth
 */
@Repository
public interface TokenRepository extends JpaRepository<Token, Long> {

  @Query("SELECT t FROM Token t LEFT JOIN Blockchain b ON t.blockchain.id = b.id WHERE t.address = :address AND b" +
      ".name = :blockchain")
  Optional<Token> findTokenByAddressAndBlockchain(String address, String blockchain);
}
